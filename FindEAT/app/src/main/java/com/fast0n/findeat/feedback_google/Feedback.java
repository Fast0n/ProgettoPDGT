package com.fast0n.findeat.feedback_google;

public class Feedback {

    public String nome;
    public String ora;
    public String feedback;
    public String iconfeedback;
    public String rating;

    public Feedback(String nome, String ora, String feedback, String iconfeedback, String rating) {
        this.nome = nome;
        this.ora = ora;
        this.feedback = feedback;
        this.iconfeedback = iconfeedback;
        this.rating = rating;
    }
}
